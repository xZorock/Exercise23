/**
 * @author Jason Di Bartolo
 * This class rapresents the method to do operations
 */
public class ArithmeticOperators {
    /**
     * This method does the sum of two numbers
     * @param num1 variable number 1 to be defined
     * @param num2 variable number 2 to be defined
     * @return return the sum of the parameters
     */
    public int sum(int num1, int num2){
        return num1 + num2;
    }

    /**
     * This method does the subtraction of two numbers
     * @param num1 variable number 1 to be defined
     * @param num2 variable number 1 to be defined
     * @return return the subtraction of the parameters
     */
    public int sub(int num1, int num2){
        return num1 - num2;
    }

    /**
     * This method does the multiplication of two numbers
     * @param num1 variable number 1 to be defined
     * @param num2 variable number 1 to be defined
     * @return return the multiplication of the parameters
     */
    public int mul(int num1, int num2){
        return num1 * num2;
    }

    /**
     * This method does the division of two numbers
     * @param num1 variable number 1 to be defined
     * @param num2 variable number 1 to be defined
     * @return return the divion of the parameters
     */
    public int div(int num1, int num2){
        return num1 / num2;
    }
}